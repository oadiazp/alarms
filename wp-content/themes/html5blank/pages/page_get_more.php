2. <b>Human Error</b> &ndash; Poor placement of sensors during setup has the potential to cause a false alarm. If a sensor is not properly aligned with the magnet, a false alarm can occur.<br>
<br>
3. <b>Cooking</b> &ndash; Excess smoke from cooking can set off a monitored smoke sensor, resulting in a false alarm. Be mindful to ensure proper ventilation when cooking certain items, especially when frying.
