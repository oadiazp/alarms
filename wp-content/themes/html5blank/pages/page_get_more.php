<strong>To bypass a door/window sensor prior to arming:<br></strong><br>
1. Before arming the system, open the desired door or window to be bypassed.<br>
2. Arm your system accordingly.<br>
3. The panel will notify you that the sensor is reporting “Open.”<br>
4. Press the Bypass button.<br>
5. The system will now arm all of the sensors except the one(s) reporting open.
